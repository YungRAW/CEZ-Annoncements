<p class = "h5"> ČEZ Group (Czech: 'Skupina ČEZ' České Energetické Závody) is a conglomerate of 96 companies (including the parent company ČEZ, a.s.), 72 of them in the Czech Republic. Its core business is the generation, distribution, trade in, and sales of electricity and heat, trade in and sales of natural gas, and coal extraction. ČEZ Group operates also in Bulgaria, Germany, Hungary, Poland, Romania, Slovakia and Turkey. ČEZ, a.s. is listed on Prague Stock Exchange and Warsaw Stock Exchange.</p>

<p class = "h5">ČEZ is the largest utility and biggest public company in Central and Eastern Europe. Its majority shareholder is the Czech government, owning 70% of shares. Its historical political activities have come under scrutiny. According to the Economist, "though nominally state-run, many see the power flowing the other way: from CEZ’s board into politics".Capital Group Companies invested 2.98% into ČEZ Group. Since 2011, when Daniel Beneš have become CEO these calls faded out. </p>

